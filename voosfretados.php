<h3>Sobre a central de vôos fretados</h3>
<br />
<p align="justify">A Central de Vôos Fretados e  Humanitários foi criada em 04 de março de 2003 e está baseada no Núcleo Belo  Horizonte, sendo dotada de rotas específicas para vôos Fretados e Humanitários  por aeronaves de grande porte para passageiros, para serem operadas por Pilotos  de todos os núcleos habilitados de acordo com o Regulamento Geral.</p>
